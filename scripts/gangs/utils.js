import {
  equipmentJsonFilename,
  recruitmentMaxWaitTimeSeconds,
  tasksJsonCombatFilename,
  tasksJsonHackingFilename,
} from "./constants.js";
import { formatGainRate, formatTimeSeconds } from "/utils/formatters.js";
import { print, printError, toGreen, toRed } from "/utils/print.js";

function checkFileExists(ns, fname, type, filename) {
  if (ns.fileExists(filename)) {
    return true;
  }

  printError(
    ns,
    `[${fname}] ${type} file ${filename} does not exist. ` +
      `Please run the gangs/start.js script to generate the ${type} file.`,
  );

  return false;
}

//#region Tasks

/**
 * Writes gang tasks to a json file for other scripts to use.
 * Assumes player has already created a gang and tasks are available.
 *
 * @param {NS} ns
 * @param {boolean} isHackingGang : Hacking / Combat gang
 */
export function writeGangTasks(ns, isHackingGang = true) {
  const fname = "writeGangTasks";
  const filename = isHackingGang
    ? tasksJsonHackingFilename
    : tasksJsonCombatFilename;

  const tasks = ns.gang.getTaskNames().map((taskName) => {
    return ns.gang.getTaskStats(taskName);
  });

  ns.write(filename, JSON.stringify(tasks, null, 2), "w");

  // Log
  const gangType = isHackingGang ? "Hacking" : "Combat";
  ns.print(`[${fname}] ${toGreen(gangType)} tasks written to ${filename}`);
}

/**
 * Reads gang tasks from a json file.
 * Assumes player has already created a gang and tasks are available.
 * Assumes the tasks file has been generated by writeGangTasks function.
 *
 * @param {NS} ns
 * @param {boolean} isHackingGang : Hacking / Combat gang
 * @returns {GangTaskStats[]|null} Array of gang task stats objects.
 *   Null if file doesn't exist.
 */
export function readGangTasks(ns, isHackingGang) {
  const fname = "readGangTasks";
  const filename = isHackingGang
    ? tasksJsonHackingFilename
    : tasksJsonCombatFilename;

  if (!checkFileExists(ns, fname, "Tasks", filename)) {
    return null;
  }

  const tasksJson = ns.read(filename);
  const tasks = JSON.parse(tasksJson);

  // Log
  const gangType = isHackingGang ? "Hacking" : "Combat";
  ns.print(`[${fname}] ${toGreen(gangType)} tasks read from ${filename}`);

  return tasks;
}

