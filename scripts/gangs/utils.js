/**
 * Finds the gang member with the min or max value of a given stat.
 *
 * @param {NS} ns - Netscript API object
 * @param {string[]} memberNames - Array of gang member names to search through
 * @param {string} stat - Property name on GangMemberInfo (e.g. "hack", "wantedLevelGain")
 * @param {"min"|"max"} direction - Whether to find the lowest or highest value
 * @returns {GangMemberInfo} The matching member info object
 * @throws {TypeError} if memberNames is empty
 */
function findMemberBy(ns, memberNames, stat, direction = "min") {
  /** @type {GangMemberInfo[]} */
  const members = memberNames.map((name) => ns.gang.getMemberInformation(name));
  return members.reduce((prev, cur) => {
    const pick =
      direction === "min" ? cur[stat] < prev[stat] : cur[stat] > prev[stat];
    return pick ? cur : prev;
  });
}

export const findMemberMinHackingLevel = (ns, memberNames) =>
  findMemberBy(ns, memberNames, "hack", "min");

export const findMemberMaxHackingLevel = (ns, memberNames) =>
  findMemberBy(ns, memberNames, "hack", "max");

export const findMemberMaxWantedLevel = (ns, memberNames) =>
  findMemberBy(ns, memberNames, "wantedLevelGain", "max");

export const findMemberMinWantedLevel = (ns, memberNames) =>
  findMemberBy(ns, memberNames, "wantedLevelGain", "min");

//#endregion Member Finder

/**
 * Finds the least productive member based on the sorted tasks list.
 * The least productive member is the one doing the task with the lowest focus gain.
 * If all members are doing the most productive task, then no member would be returned.
 *
 * @param {NS} ns
 * @param {string[]} memberNames
 * @param {GangTaskStats[]} sortedTasks - List of tasks sorted by focus gain (ascending)
 * @returns {{ member: GangMemberInfo, taskIdx: number }}
 *    member: The member doing the least productive task.
 *      Null if all members are doing the most productive task.
 *    taskIdx: The index of the task in the sortedTasks array.
 */
export function findLeastProductiveMember(ns, memberNames, sortedTasks) {
  const fname = "findLeastProductiveMember";

  /** @type {GangMemberInfo[]} */
  const members = memberNames.map((memberName) =>
    ns.gang.getMemberInformation(memberName),
  );

  let lowestGainingMember = null;
  let lowestTaskIdx = sortedTasks.length - 1;
  for (const member of members) {
    const taskIndex = sortedTasks.findIndex(
      (task) => task.name === member.task,
    );

    if (taskIndex === -1) {
      throw new Error(
        `[${fname}] Member ${member.name} is doing an unknown task: ${member.task}`,
      );
    }

    if (taskIndex < lowestTaskIdx) {
      lowestTaskIdx = taskIndex;
      lowestGainingMember = member;
    }
  }
  return { member: lowestGainingMember, taskIdx: lowestTaskIdx };
}

//#region Ascend

export function getShouldWaitAscend(ns, respectNeeded, respectGainRate) {
  const fname = "getShouldWaitAscend";

  if (respectGainRate <= 0) {
    // Respect is not increasing, no reason to wait to ascend
    return false;
  }

  // respect gain rate is per game cycle
  const respectGainRatePerSecond = respectGainRate * 5;
  const timeToNextRecruitSeconds = respectNeeded / respectGainRatePerSecond;

  const fmtRespectNeeded = ns.formatNumber(respectNeeded);
  const fmtRespectGainRate = formatGainRate(respectGainRatePerSecond);
  const fmtTimeToNextRecruit = formatTimeSeconds(timeToNextRecruitSeconds);
  ns.print(
    `[${fname}] Respect needed: ${fmtRespectNeeded}, gain rate: ${fmtRespectGainRate} ` +
      `=> Time to next recruit: ${fmtTimeToNextRecruit}.`,
  );

  return timeToNextRecruitSeconds <= recruitmentMaxWaitTimeSeconds;
}

